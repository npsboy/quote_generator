function get_random_item(items) {
  return items[Math.floor(Math.random()*items.length)];  
}

function make_quote (type_of_quote)  {
  let selected = wordsAndPhrases.find(item => item.type === type_of_quote);
  if (type_of_quote === "is") {
    let part1 = get_random_item(selected.list1)
    let part2 = get_random_item(selected.list2)
    return part1 + " is " + part2 + "."
  }
  else if (type_of_quote === "if") {
    let part1 = get_random_item(selected.list1)
    let part2 = get_random_item(selected.list2)
    return part1 + ", " + part2 + "."
  }
  else if (type_of_quote === "like") {
    let part1 = get_random_item(selected.list1)
    let part2 = get_random_item(selected.list2)
    return part1 + " - " + part2 + "."
  }
  else if (type_of_quote === "not") {
    let part1 = get_random_item(selected.list1)
    let part2 = get_random_item(selected.list2)
    let random_no = Math.floor(Math.random() * 5) + 1;
    if (random_no == 1){
      return "Life is not about " + part1 + ". It's about " + part2 + "."
    }
    else if (random_no == 2){
      return "Success is not achieved through " + part1 + ". It comes from " + part2 + "."
    }
    else if (random_no == 3){
      return "True fulfillment is not found in " + part1 + ". It lies in " + part2 + "."
    }
    else if (random_no == 4){
      return "Many think that life is about " + part1 + ". What actually matters is " + part2 + "."
    }
    else {
      return "I've talked to many people who think success is about " + part1 + ". What they don't realize is that its about " + part2 + "."
    }
  }
} 

function get_author (){
    let first_name = get_random_item(first_names)
    let last_name = get_random_item(last_names)
    return first_name + " " + last_name
}

function main()  {
    let types = [ "is", "if", "like", "not"]
    let selected_type = get_random_item(types)
    let quote = make_quote (selected_type)
    let author = get_author()
    let full_quote = ("\"" + quote + "\"")
    console.log (full_quote + " -" + author)
    document.getElementById("quote").innerHTML = "<mark>" + full_quote + "</mark>";
    document.getElementById("author").innerHTML = "-" + author;
}

main();